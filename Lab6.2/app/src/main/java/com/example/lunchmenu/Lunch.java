package com.example.lunchmenu;

public class Lunch {

    private final String lunch1;
    private final String lunch2;

    public Lunch(String lunch1, String lunch2) {
        this.lunch1 = lunch1;
        this.lunch2 = lunch2;
    }

    public String getLunch1() {
        return lunch1;
    }

    public String getLunch2() {
        return lunch2;
    }
}
